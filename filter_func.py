# Функция filter получает набор элементов для фильтрации и фильтрующую функцию,
# определяющую- нужно ли исключить конкретный элемент из данной последовательности.
# 'filter(предикативная_функция, последовательность'

is_even = lambda x: x % 2 == 0

seq = (1, 2, 3, 4, 5, 6, 7, 8)
filtered = filter(is_even, seq)
print(filtered)
print(list(filtered))
